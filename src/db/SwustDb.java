package db;

public class SwustDb {
	private String className;
	
	private String classNum;
	
	private String classGrade;
	
	private String classChar;
	
	private String classScore;
	
	private String classAgain;
	
	private String classGDP;

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getClassNum() {
		return classNum;
	}

	public void setClassNum(String classNum) {
		this.classNum = classNum;
	}

	public String getClassGrade() {
		return classGrade;
	}

	public void setClassGrade(String classGrade) {
		this.classGrade = classGrade;
	}

	public String getClassChar() {
		return classChar;
	}

	public void setClassChar(String classChar) {
		this.classChar = classChar;
	}

	public String getClassScore() {
		return classScore;
	}

	public void setClassScore(String classScore) {
		this.classScore = classScore;
	}

	public String getClassAgain() {
		return classAgain;
	}

	public void setClassAgain(String classAgain) {
		this.classAgain = classAgain;
	}

	public String getClassGDP() {
		return classGDP;
	}

	public void setClassGDP(String classGDP) {
		this.classGDP = classGDP;
	}
	
	
}
